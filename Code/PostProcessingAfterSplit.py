    program = ""
    if Language == "Java":
        program = addJavaStructure(functions)
    elif Language == "Python":
        program = addPythonStructure(functions)
    elif Language == "Javascript":
        program = addJavascriptStructure(functions)
    elif Language == "C++":
        program = addCPPStructure(functions)
    elif Language == "Haskell":
        program = addHaskellStructure(functions)
    elif Language == "Pascal":
        program = addPascalStructure(functions)
    elif Language == "C#":
        program = addCScharfStructure(functions)
    elif Language == "C":
        program = addCStructure(functions)
    return program


