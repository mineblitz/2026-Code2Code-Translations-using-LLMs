def removeBackticks(response):
    lines = response.splitlines()
    processed_lines = []
    skip_line = False
    skip_Next_line = False
    bracketRemovalNext = False
    classFound = False
    for line in lines:
        if '```' in line:
            if not skip_line:
                skip_line = True  # Start skipping lines after the first occurrence of backticks
            continue  # Skip the current line containing backticks

        # removing imports
        if "using" in line: continue
        if "#include" in line: continue
        if "import" in line: continue
        if "uses" in line: 
            skip_Next_line = True
            continue
        if skip_Next_line: 
            skip_Next_line = False
            continue
        # removing classes
        if "class" in line:
            classFound = True
            if not "{" in line:
                bracketRemovalNext = True
            continue
        if bracketRemovalNext:
            bracketRemovalNext = False
            continue
            
        
        if skip_line:
            # If we are skipping lines, stop once we encounter another line with backticks
            if '```' in line:
                break
            else:            
                processed_lines.append(line)
        
    if classFound:
        processed_lines = processed_lines[:-1]
    return '\n'.join(processed_lines).strip()


